let currentLang = localStorage.getItem("lang") || "en";

function applyTranslation(lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (i18n[key] && i18n[key][lang]) {
      if (key === "logo") {
        el.innerHTML = i18n[key][lang];
      } else {
        el.innerHTML = i18n[key][lang];
      }
    }
  });
  document.documentElement.lang = lang === "ar" ? "ar" : "en";
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.className = document.body.className.replace(/lang-(en|ar)/g, "").trim();
  document.body.classList.add("lang-" + lang);
  const toggle = document.getElementById("langToggle");
  if (toggle) {
    toggle.textContent = lang === "en" ? "AR" : "EN";
    toggle.classList.toggle("active", lang === "ar");
  }
  if (lang === "ar") {
    document.querySelector(".hero-name").style.fontFamily = "'Cairo', sans-serif";
  } else {
    document.querySelector(".hero-name").style.fontFamily = "";
  }
}

